Si ya enviaste dinero a alguien que conociste en línea y aún no has conocido en persona, no eres estúpido. Encontraste un sistema diseñado específicamente para producir exactamente la situación en que te encontraste.

Qué hacer ahora:
1. **Deja de enviar dinero.** La siguiente solicitud llegará.
2. **Contacta a tu banco inmediatamente.** Algunas transferencias pueden revertirse dentro de 24-48 horas.
3. **No confrontes a la persona directamente.** La confrontación puede desencadenar un enfoque de manipulación diferente.
4. **Cuéntaselo a alguien de confianza.**
5. **Reporta el perfil.**

> [!TIP]
> Una relación genuina — una con un futuro real — no se construye sobre asistencia financiera. Se construye sobre tiempo, conversación, presencia y eventualmente experiencia compartida en el mismo espacio físico. La persona correcta no te pedirá que rompas esta regla.
