## The Simple Rule: Never Send Money in the First Six Months

This is not a complex rule. It does not require nuance or case-by-case evaluation. It applies regardless of how long you have been talking, how genuine the person seems, how urgent the situation appears, or how much you care about them.

**Never send money, gift cards, cryptocurrency, or financial assistance of any kind to someone you have not yet met in person, and ideally not in the first six months of any relationship regardless.**

This rule will be tested. The scenario that tests it will feel different from a scam. It will feel like a real crisis, a real person, a real need that you are uniquely positioned to meet. That feeling is exactly how the rule gets broken.

### Why the Rule Exists

Romance scammers study human psychology. They understand that the moment a financial request feels like a logical response to a real situation — rather than an inexplicable ask from a stranger — is the moment most people are most vulnerable.

The rule exists because there is no financial crisis in someone's life that can only be solved by a person they have never met in person sending money across international borders. This is always true. There are family members, local resources, employers, banks, NGOs, and community networks that exist in every country to address financial emergencies. The scenario where an international online contact is the only option is not a real scenario. It is a constructed one.

> [!WARNING]
> This does not mean that everyone who asks for financial help online is a scammer. It means that the act of sending money before meeting someone in person creates a dynamic — one of financial dependency and obligation — that undermines the foundation of a genuine relationship regardless of the other person's intentions. A relationship that begins with financial assistance is a relationship that begins with an imbalance. That imbalance rarely resolves.

### The Most Common Request Scenarios

Understanding the scenarios helps you recognize them without being surprised.

**Medical emergency.** A family member is ill. The treatment is needed immediately. Insurance does not cover it. The local hospital requires payment before treating. The family is exhausted their resources. Your help would be the difference between treatment and suffering.

**Travel emergency.** She is about to visit you — finally. The ticket is booked, everything is arranged. Then something goes wrong: the ticket was cancelled, there is a visa fee that was not expected, she lost her wallet, the airline requires a rebooking fee that her local bank will not advance.

**Business crisis.** She (or he) runs a small business. A major deal has fallen through. The inventory was already ordered. The loan payment is due. Just this once — and she will pay it back as soon as the situation resolves.

**Equipment failure.** He is an engineer on a remote project. His equipment broke. The project cannot continue until it is replaced. He is owed significant payment when the project completes, but he cannot continue without the tool. The company is not covering the replacement quickly enough.

**Investment opportunity.** This one arrives differently — not as a request, but as a gift: she wants to share her success with you. A trusted broker. A special account. Early gains that look real.

> [!IMPORTANT]
> If any of these scenarios appears in your online relationship, the correct response is not to send money. It is to end or pause the conversation and report the profile. You do not need to investigate, verify, or determine whether this particular person is really a scammer. You need to apply the rule. The rule is not about evaluating their honesty. It is about protecting yourself from a dynamic that always ends badly.

### If You Have Already Sent Money

This is the part of the guide that is hardest to read, but most important for some people.

If you have already sent money to someone you met online and have not yet met in person, you are not stupid. You are not naive in some unusual way. You encountered a system specifically designed to produce exactly the situation you found yourself in. These operations are professional and they are effective.

What to do now:

1. **Stop sending money.** The next request will come. It always does. Stopping is the only intervention that works.

2. **Contact your bank or payment provider immediately.** If you sent money recently (within 24-48 hours), some transfers can be reversed. Contact your bank and explain the situation. They have seen it before.

3. **Do not confront the person directly.** Confrontation gives them information about your level of awareness and may trigger a different manipulation approach — the hurt, devastated response designed to make you feel guilty enough to continue.

4. **Tell someone you trust.** The isolation that comes with this situation — the shame, the difficulty of admitting what happened — is part of the design. Break it by talking to someone.

5. **Report the profile.** You will protect other people. The report is not a formal legal accusation. It is information that allows the platform to investigate and remove accounts.

> [!TIP]
> If you are struggling with the emotional aftermath of a romance scam — which is real and can be significant — the AARP Fraud Watch Network (US) and similar organizations in other countries provide confidential support specifically for people who have experienced financial fraud involving emotional manipulation. You do not have to process this alone.

### The Rule as a Sign of Respect

There is a way of understanding this rule that goes beyond self-protection. A genuine relationship — one that has a real future — is not built on financial assistance. It is built on time, conversation, presence, and eventually shared experience in the same physical space.

Holding the rule is not a sign of distrust toward a specific person. It is a sign of respect for what a real relationship actually requires. The right person will not ask you to break it. And if they do, you will know something important.
