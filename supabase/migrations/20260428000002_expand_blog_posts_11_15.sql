## La Regla Simple: Nunca Envíes Dinero en los Primeros Seis Meses

Esta no es una regla compleja. No requiere matices ni evaluación caso por caso. Se aplica independientemente de cuánto tiempo lleven hablando, cuán genuina parezca la persona, cuán urgente parezca la situación o cuánto te importe.

**Nunca envíes dinero, tarjetas de regalo, criptomonedas ni ningún tipo de asistencia financiera a alguien que aún no has conocido en persona, e idealmente no en los primeros seis meses de cualquier relación.**

Esta regla será puesta a prueba. El escenario que la pruebe se sentirá diferente a una estafa. Se sentirá como una crisis real, una persona real, una necesidad real que tú estás en una posición única para satisfacer. Esa sensación es exactamente cómo se rompe la regla.

### Por qué Existe la Regla

Los estafadores románticos estudian la psicología humana. Entienden que el momento en que una solicitud financiera parece una respuesta lógica a una situación real es el momento en que la mayoría de las personas son más vulnerables.

La regla existe porque no hay ninguna crisis financiera en la vida de alguien que solo pueda resolverse con que una persona que nunca han conocido en persona envíe dinero a través de fronteras internacionales.

> [!WARNING]
> Esto no significa que todos los que pidan ayuda financiera en línea sean estafadores. Significa que el acto de enviar dinero antes de conocer a alguien en persona crea una dinámica — de dependencia y obligación financiera — que socava la base de una relación genuina independientemente de las intenciones de la otra persona.

### Los Escenarios de Solicitud más Comunes

- **Emergencia médica:** Un familiar está enfermo. El tratamiento se necesita inmediatamente.
- **Emergencia de viaje:** Está a punto de visitarte. Luego algo sale mal: el boleto fue cancelado, hay una tarifa de visa inesperada.
- **Crisis empresarial:** Dirige un pequeño negocio. Un trato importante falló. Solo esta vez — y lo devolverá tan pronto como la situación se resuelva.
- **Oportunidad de inversión:** No llega como una solicitud, sino como un regalo: quiere compartir su éxito contigo.

> [!IMPORTANT]
> Si cualquiera de estos escenarios aparece en tu relación en línea, la respuesta correcta no es enviar dinero. Es terminar o pausar la conversación y reportar el perfil. La regla no se trata de evaluar su honestidad. Se trata de protegerte de una dinámica que siempre termina mal.

