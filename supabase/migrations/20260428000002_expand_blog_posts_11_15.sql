### Por qué Existe la Verificación Personal

Las verificaciones estándar comprueban lo que se puede comprobar de forma remota y automática. Pero no pueden capturar el cuadro más completo del carácter, la situación y las intenciones de una persona.

La verificación personal llena ese hueco con un elemento humano: una conversación real con un miembro del equipo de MatchVenezuelan.

