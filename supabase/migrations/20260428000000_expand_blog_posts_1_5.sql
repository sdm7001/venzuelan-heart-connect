This guide explains what each badge means, how we conduct each verification, and what the presence or absence of a badge should tell you.

### Why Verification Matters More in International Dating

In a local dating context, the social cost of misrepresentation is relatively high. You have mutual friends, you might work in overlapping industries, you might run into this person at a shared venue. These ambient pressures create a soft accountability.

In international online dating, those pressures disappear. A person can present almost any identity, claim almost any life, and the person on the other side has very limited means of checking. This is not unique to Venezuelan dating — it is a structural feature of any international platform — but it does mean that formal verification carries more weight than it would in a local context.

> [!IMPORTANT]
> No verification system eliminates risk entirely. What verification does is raise the cost of deception significantly. Someone who has submitted government ID, participated in a live video check, and built a profile history over months has invested considerably more than someone who created an account yesterday. This investment is itself a signal.

### The Photo Verification Badge

**What it means:** The person has submitted at least three photos to our moderation team, and those photos have passed our review process. Specifically:
- Photos are not reverse-image-search matches to stock photos, celebrities, or other profiles on public platforms
- Photos appear to be of the same person (facial consistency across images)
- No obvious digital manipulation that changes fundamental appearance

**What it does not mean:** That the person looks exactly like their photos in real life, that the photos are recent (though we flag profiles where photos appear significantly aged), or that the person is who they claim to be — photo verification is a consistency check, not an identity check.

**When it matters most:** Photo verification is the baseline. Profiles without it have provided the minimum evidence that they are a real person. You should be cautious about investing heavily in communication with unverified profiles.

### The ID Verification Badge

**What it means:** The person has submitted a government-issued identity document — passport, national identity card, or equivalent — and our verification team has confirmed that the document appears genuine and that the name on the document matches the profile. A live selfie at the time of submission is cross-checked against the ID photo.

**What it does not mean:** That everything else on the profile is accurate. A verified name does not verify an occupation, a location, a relationship history, or intentions. It does confirm that a real human being with a real documented identity is connected to this account.

> [!TIP]
> An ID-verified profile is meaningfully more reliable than an unverified one. When you are deciding whether to invest in longer calls, share your own contact information, or consider meeting in person, this badge should be part of your calculus.

### The Phone Verification Badge

**What it means:** The account is linked to a phone number that received and confirmed an SMS code at the time of signup. The number was active at that moment.

**What it does not mean:** That the phone number is a personal number that belongs to the profile holder. Phone numbers can be purchased cheaply in many countries, including prepaid SIMs. Phone verification is a basic signal — it eliminates automated bot accounts effectively — but it is the weakest of the trust indicators.

**In practice:** Almost every legitimate profile on the platform has phone verification. Its absence is a red flag. Its presence alone is not a strong positive signal.

### The Video Call Badge

**What it means:** This person has completed at least one live video session, either as part of concierge verification or through our peer video call logging system (where both users confirm that a live call took place and that the person on screen matched their profile photos).

**Why this matters:** Video calls are the single hardest element of a profile to fake. A photo can be borrowed. A bio can be invented. A live video conversation, conducted without a script and with the expectation of natural interaction, is very difficult to sustain under false pretenses for any length of time.

> [!IMPORTANT]
> If someone you have been communicating with for weeks consistently avoids or cancels video calls, that pattern is more informative than any badge. Legitimately busy people occasionally reschedule. People who are misrepresenting themselves systematically avoid the format that would expose the misrepresentation.

### The Concierge Verification Badge

**What it means:** This profile has completed our highest level of verification. A member of the MatchVenezuelan team has:
- Conducted a live video interview with the member
- Verified their identity against their submitted documents
- Confirmed their stated location
- Confirmed their stated occupation or life situation to a reasonable degree
- Reviewed their communication history on the platform for patterns inconsistent with genuine engagement

A concierge-verified profile is as close to a confirmed real person with confirmed intent as a dating platform can offer.

**Who completes concierge verification:** Primarily members who are committed to the platform and want to signal serious intent to potential connections. The process takes time and requires cooperation. It is not something a scammer with a borrowed identity would voluntarily submit to.

> [!TIP]
> If you are serious about finding a genuine connection, filtering your search to concierge-verified profiles is a reasonable starting point. You will see fewer results, but the quality of those results is meaningfully higher.

### The Activity Badge

**What it means:** This profile has been active on the platform within the last 14 days. It does not indicate the nature of that activity — browsing, sending messages, or updating their profile all count.

**What it does not mean:** That the person is actively seeking a new connection right now. Someone can maintain platform activity while already in an early conversation they are investing in, or while taking a slower approach to browsing.

**In practice:** A profile that has not been active in several months is less likely to respond to a message. This is simple logistics, not a signal about the person's character.

### Reading Combinations of Badges

Individual badges matter; combinations matter more. A profile with ID verification, video call verification, and concierge verification is presenting a substantially higher level of committed evidence than a profile with photo verification alone.

Think of it as a spectrum rather than a binary. No badge: proceed with significant caution. Photo only: proceed carefully, get to a video call early. Photo + ID: reasonable basis for extended communication. All badges including concierge: the highest confidence level the platform can offer.

> [!WARNING]
