### What Verification Cannot Tell You

Verification confirms identity and some aspects of circumstances. It cannot confirm character, intentions, compatibility, or readiness for a relationship. A fully verified profile can belong to someone who is genuinely the person they claim to be and still not be a good match for you. Verification clears away the most obvious obstacles to trust. The rest — the part that actually matters — is still up to the conversation.
