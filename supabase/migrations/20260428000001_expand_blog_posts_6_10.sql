## La Primera Videollamada: Corta, Amable y Sorprendentemente Esclarecedora

La primera videollamada es uno de los momentos más importantes en una conexión en línea — y uno de los más mal manejados. Es importante porque es la primera vez que la otra persona existe en forma real y en tiempo real en lugar de como una colección de fotos y textos. Se maneja mal porque la mayoría de las personas la abordan con demasiada actuación o demasiado poca preparación.

### Qué es Realmente una Primera Videollamada

Es una confirmación y un comienzo. Confirma que ambas personas son reales, están presentes e interesadas en continuar. Comienza el proceso de conocerse en un registro más completo: voz, expresión, ritmo, la textura de una conversación real.

> [!TIP]
> Establece un límite de tiempo para la primera llamada antes de comenzar — dile explícitamente que la mantendrás en 30-40 minutos. Esto elimina la presión de ambos lados. Una primera llamada que termina en un punto natural y deja a ambas personas queriendo más es mucho más exitosa que una que dura dos horas y se queda sin energía.

### Preparación que Realmente Ayuda

