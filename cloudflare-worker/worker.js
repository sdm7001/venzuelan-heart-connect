class HtmlLangHandler {
  constructor(lang) { this.lang = lang; }
  element(el) { el.setAttribute("lang", this.lang); }
}

class HeadMetaInjector {
  constructor(meta) { this.meta = meta; this.injected = false; }

  element(el) {
    if (this.injected) return;
    this.injected = true;
    const { title, description, canonical, alternates, jsonLd } = this.meta;

    let html = "";

    // Title
    html += `<title>${escapeHtml(title)}</title>`;

    // Description
    html += `<meta name="description" content="${escapeHtml(description)}">`;

    // Canonical
    html += `<link rel="canonical" href="${escapeHtml(canonical)}">`;

    // og:url
    html += `<meta property="og:url" content="${escapeHtml(canonical)}">`;

    // og:title / og:description (overwrite what static shell has)
    html += `<meta property="og:title" content="${escapeHtml(title)}">`;
    html += `<meta property="og:description" content="${escapeHtml(description)}">`;

    // hreflang alternates
    for (const alt of (alternates ?? [])) {
      html += `<link rel="alternate" hreflang="${escapeHtml(alt.hreflang)}" href="${escapeHtml(alt.href)}">`;
    }

    // JSON-LD (only for pages that have it)
    if (jsonLd) {
      html += `<script type="application/ld+json">${JSON.stringify(jsonLd)}</script>`;
    }

    // Inject BEFORE </head> so it appears early in the head
    el.prepend(html, { html: true });
  }
}

function escapeHtml(str) {
  return String(str)
    .replace(/&/g, "&amp;")
    .replace(/"/g, "&quot;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

