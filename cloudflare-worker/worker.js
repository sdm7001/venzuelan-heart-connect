export default {
  async fetch(request, env, ctx) {
    const url = new URL(request.url);
    const pathname = normalizePath(request.url);

    // Pass through assets unchanged — no HTML rewriting needed
    if (isAsset(pathname)) {
      return fetch(request);
    }

    // Fetch the response from origin (Netlify)
    const response = await fetch(request);

    // Only rewrite HTML responses
    if (!isHtmlRequest(request, response)) {
      return response;
    }

    const meta = resolveMeta(pathname);

    // Use HTMLRewriter to stream-transform the response
    return new HTMLRewriter()
      .on("html", new HtmlLangHandler(meta.lang))
      .on("head", new HeadMetaInjector(meta))
      .transform(response);
  },
};
