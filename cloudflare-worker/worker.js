function normalizePath(url) {
  const { pathname } = new URL(url);
  // Strip trailing slash except for root
  return pathname.length > 1 ? pathname.replace(/\/$/, "") : pathname;
}

function resolveMeta(pathname) {
  // Direct match first
  if (META_MAP[pathname]) return META_MAP[pathname];
  // Pattern fallback
  for (const pattern of META_MAP_PATTERNS) {
    if (pattern.match(pathname)) return pattern.build(pathname);
  }
  // Generic fallback — homepage metadata (avoids broken canonical on 404s etc.)
  return META_MAP["/"];
}

function isHtmlRequest(request, response) {
  const accept = request.headers.get("accept") ?? "";
  const ct = response.headers.get("content-type") ?? "";
  // Bots often don't send Accept headers, so also check content-type
  return accept.includes("text/html") || ct.includes("text/html");
}

function isAsset(pathname) {
  return /\.(js|css|webp|jpg|jpeg|png|gif|svg|ico|woff2?|ttf|map|json|txt|xml)$/i.test(pathname);
}

