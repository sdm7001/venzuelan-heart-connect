/**
 * MatchVenezuelan Cloudflare Worker — Pre-JS SEO Injection
 *
 * Architecture:
 *   Netlify origin → Cloudflare CDN → this Worker → client/bot
 *
 * What it does:
 *   For every HTML response (pages, not assets), it rewrites the <head> using
 *   HTMLRewriter to inject:
 *     - <html lang="..."> attribute (en or es based on path)
 *     - <title>
 *     - meta description
 *     - <link rel="canonical">
 *     - <link rel="alternate" hreflang="..."> pairs
 *     - JSON-LD structured data (highest-value pages only)
 *
 *   This ensures Google and social crawlers see correct per-page metadata
 *   without needing to execute JavaScript.
 *
 * Why not SSR/SSG:
 *   The app is a Vite CSR SPA. Adding a Worker is the fastest, lowest-risk
 *   path to pre-JS SEO — no rebuild pipeline changes, no framework migration.
 *
 * Maintenance:
 *   Run `npm run generate:worker-meta` to regenerate META_MAP from source.
 *   See scripts/generate-worker-meta.mjs for details.
 */

